# Test 5: MONTHLY_SPIKE
# ---------------------------------------------------------------------------

def test_monthly_spike(result: EvalResult) -> None:
    print("\n[5] Detector: MONTHLY_SPIKE")

    svc = BankStatementService()
    # 3 baseline months at ~50K debit, then a 300K debit month
    txns = []
    for day, month in [(10, 4), (15, 5), (20, 6)]:
        txns.append({
            "date": f"2025-{month:02d}-{day:02d}",
            "description": "NEFT-N000-VENDOR",
            "debit": "50000.00", "credit": "0", "mode": "NEFT", "balance": "100000",
        })
    txns.append({
        "date": "2025-07-15",
        "description": "NEFT-N001-LARGE VENDOR",
        "debit": "300000.00", "credit": "0", "mode": "NEFT", "balance": "50000",
    })
    analysis = svc.analyze(txns, fy_start=date(2025, 4, 1), fy_end=date(2026, 3, 31))
    fired = any(f["category"] == "MONTHLY_SPIKE" for f in analysis["flags"])
    result.check("MONTHLY_SPIKE fires when month debit > 2x trailing 3-month avg", fired)


# ---------------------------------------------------------------------------
# Test 6: Categorizer + counterparty on canonical narrations
# ---------------------------------------------------------------------------

def test_categorizer_and_counterparty(result: EvalResult) -> None:
    print("\n[6] Enrichment: categorizer + counterparty extraction")

    from services.bank_enrichment import categorize, extract_counterparty

    cases = [
        ("NEFT-SBIN0001234-RAJESH KUMAR SALARY", 75000.0, True, "SALARY", "NEFT", "RAJESH KUMAR SALARY"),
        ("UPI-123456-SWIGGY-PYTM", 450.0, False, "PERSONAL_EXPENSE", "UPI", None),
        ("NACH-HDFCLOAN-EMI-HOMELOAN", 52000.0, False, "EMI_LOAN_REPAYMENT", "NACH", None),
        ("GST PAYMENT CHALLAN CPIN12345", 32000.0, False, "GST_PAYMENT", None, None),
    ]
    for desc, amt, is_credit, expected_cat, expected_cp_type, expected_cp_name in cases:
        cat, _ = categorize(desc, amt, is_credit)
        cp = extract_counterparty(desc)
        result.check(
            f"categorize({desc[:40]!r}) -&gt; {expected_cat}",
            cat == expected_cat,
            f"got {cat}",
        )
        if expected_cp_type:
            result.check(
                f"counterparty type for {desc[:40]!r} == {expected_cp_type}",
                cp.get("type") == expected_cp_type,
                f"got {cp.get('type')}",
            )
        if expected_cp_name:
            result.check(
                f"counterparty name for {desc[:40]!r} contains {expected_cp_name!r}",
                expected_cp_name in (cp.get("name") or ""),
                f"got {cp.get('name')}",
            )


# ---------------------------------------------------------------------------
# Test 7: Holiday detection
# ---------------------------------------------------------------------------

def test_holidays(result: EvalResult) -> None:
    print("\n[7] Holiday detection")

    from services.bank_enrichment.holidays import is_bank_holiday, is_non_working_day

    result.check("2025-10-02 (Gandhi Jayanti) is a bank holiday", is_bank_holiday(date(2025, 10, 2)))
    result.check("2025-06-29 (Sunday) is non-working", is_non_working_day(date(2025, 6, 29)))
    result.check("2025-06-14 (2nd Saturday) is non-working", is_non_working_day(date(2025, 6, 14)))
    result.check("2025-06-16 (Mon) is working", not is_non_working_day(date(2025, 6, 16)))


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> int:
    print("=" * 70)
    print("Bank Statement Analyzer — Accuracy Evaluation")
    print("=" * 70)

    result = EvalResult()
    test_real_pdf(result)
    test_structuring(result)
    test_round_trip(result)
    test_non_working_day(result)
    test_monthly_spike(result)
    test_categorizer_and_counterparty(result)
    test_holidays(result)

    print()
    print("=" * 70)
    return result.summary()


if __name__ == "__main__":
    sys.exit(main())
